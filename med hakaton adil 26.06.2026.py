import pdfplumber
import pandas as pd

# загружаем наш PDF файл
pdf_filename = "Клиника 1 2026.pdf"

все_данные = []
текущий_раздел = "Без раздела"

print("читаю файл...")

with pdfplumber.open(pdf_filename) as pdf:
    for номер_страницы, страница in enumerate(pdf.pages, start=1):
        таблицы = страница.extract_tables()
        
        for таблица in таблицы:
            for строка in таблица:
                
                # убираем пустые ячейки
                строка = [ячейка.strip() if ячейка else "" for ячейка in строка]
                
                # если строка вообще пустая - пропускаем
                if not any(строка):
                    continue
                
                # если это название раздела (одна ячейка заполнена)
                if len(строка) > 1 and строка[0] and not строка[1]:
                    текущий_раздел = строка[0]
                    continue
                
                # если это нормальная строка с услугой
                if len(строка) >= 3:
                    код = строка[0]
                    название = строка[1]
                    цена = строка[2]
                    
                    # пропускаем шапку таблицы
                    if "Наименование" in название or "Код" in код:
                        continue
                    
                    # добавляем в наш список
                    все_данные.append({
                        "Раздел": текущий_раздел,
                        "Код": код,
                        "Название услуги": название,
                        "Цена": цена,
                        "Страница": номер_страницы
                    })

# делаем таблицу из всех данных
таблица_результат = pd.DataFrame(все_данные)

# смотрим что получилось
print(f"всего найдено строк: {len(таблица_результат)}")
print(таблица_результат.head(20).to_string())

# сохраняем в эксель
таблица_результат.to_excel("результат.xlsx", index=False)
print("готово! файл сохранён как результат.xlsx")
